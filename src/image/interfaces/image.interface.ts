/**
 * Interface that handles all the `image` entity properties.
 */
export interface IImage {
  /**
   * Property that defines a `string` that represents the `image` url.
   */
  url: string

  /**
   * Property that defines a `string` that represents the `image`
   * title. It can be used with `alt` tag for accessibility.
   */
  title: string
}
